package EntregaEjercicioCuatro;

public class SmartDevice {
	
	
	String empresa;
	String modelo;
	int numSerie;
	double precio;
	
	
public SmartDevice() {}

public SmartDevice(String empresa,String modelo,int numSerie,double precio) {
	this.empresa=empresa;
	this.modelo=modelo;
	this.numSerie=numSerie;
	this.precio=precio;
}

}
